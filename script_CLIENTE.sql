-- ? Tabla CLIENTE
CREATE TABLE CLIENTE (
    ID_CLIENTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    DOCUMENTO VARCHAR2(20) UNIQUE,
    TELEFONO VARCHAR2(20),
    EMAIL VARCHAR2(100),
    FECHA_REGISTRO DATE DEFAULT SYSDATE
);

--? Tabla INGRESO_ECONOMICO
CREATE TABLE INGRESO_ECONOMICO (
    ID_INGRESO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CLIENTE NUMBER,
    SALARIO NUMBER(10,2),
    OTROS_INGRESOS NUMBER(10,2),
    TOTAL_INGRESOS NUMBER(10,2),
    FECHA_CARGA DATE DEFAULT SYSDATE,
    
    CONSTRAINT FK_CLIENTE
    FOREIGN KEY (ID_CLIENTE)
    REFERENCES CLIENTE(ID_CLIENTE)
);

